import pytest

@pytest.mark.parametrize(
    'timestamp,hour',
    (
        (63071999, 23),
        (63072000, 0),
        (63075599, 0),
        (63075600, 1),
        (63079199, 1),
        (63079200, 2),
        (63082799, 2),
        (63082800, 3),
        (63086399, 3),
        (63086400, 4),
        (63089999, 4),
        (63090000, 5),
        (63093599, 5),
        (63093600, 6),
        (63097199, 6),
        (63097200, 7),
        (63100799, 7),
        (63100800, 8),
        (63104399, 8),
        (63104400, 9),
        (63107999, 9),
        (63108000, 10),
        (63111599, 10),
        (63111600, 11),
        (63115199, 11),
        (63115200, 12),
        (63118799, 12),
        (63118800, 13),
        (63122399, 13),
        (63122400, 14),
        (63125999, 14),
        (63126000, 15),
        (63129599, 15),
        (63129600, 16),
        (63133199, 16),
        (63133200, 17),
        (63136799, 17),
        (63136800, 18),
        (63140399, 18),
        (63140400, 19),
        (63143999, 19),
        (63144000, 20),
        (63147599, 20),
        (63147600, 21),
        (63151199, 21),
        (63151200, 22),
        (63154799, 22),
        (63154800, 23),
        (63158400, 0),
    ),
)
def test_get_hour_from_timestamp(deployed_contracts, timestamp, hour):
    crontab = deployed_contracts.DateTime
    assert crontab.getHour(timestamp) == hour
